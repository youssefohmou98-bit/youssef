let currentLanguage = localStorage.getItem('language') || 'ar';

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        if (Translations[key] && Translations[key][currentLanguage]) {
            if (el.tagName === 'INPUT' && el.type === 'text') el.placeholder = Translations[key][currentLanguage];
            else if (el.tagName === 'INPUT' && el.type === 'submit') el.value = Translations[key][currentLanguage];
            else el.textContent = Translations[key][currentLanguage];
        }
    });
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.dataset.lang === currentLanguage) btn.classList.add('active');
    });
}

function t(key) { return Translations[key]?.[currentLanguage] || key; }
window.setLanguage = setLanguage;
window.t = t;